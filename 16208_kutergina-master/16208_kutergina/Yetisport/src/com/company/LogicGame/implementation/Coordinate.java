package com.company.LogicGame.implementation;

public class Coordinate {
        private double X;
        private double Y;

        public Coordinate(double X, double Y){
            this.X = X;
            this.Y = Y;
        }

        public void setX(double x) {
            X = x;
        }

        public void setY(double y) {
            Y = y;
        }

        public double getX() {
            return X;
        }

        public double getY() {
            return Y;
        }

    }


